// Функция для получения всех доступных маршрутов
export function getAllAvailableRoutes(): string[] {
  return Object.keys(PRICE_DATA);
}

// Функция для проверки существования маршрута
export function routeExists(routeSlug: string): boolean {
  return routeSlug in PRICE_DATA;
}

// Функция для получения цены по маршруту
export function getPriceByRoute(routeSlug: string): {
  comfort: number;
  luxury: number;
  found: boolean;
} {
  const price = PRICE_DATA[routeSlug];
  if (price) {
    return {
      ...price,
      found: true,
    };
  }
  return {
    comfort: 0,
    luxury: 0,
    found: false,
  };
}
